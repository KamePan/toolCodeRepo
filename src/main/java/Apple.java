

public class Apple {
    int price;
    int like;

    public Apple() {

    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getLike() {
        return like;
    }

    public void setLike(int like) {
        this.like = like;
    }

    public Apple(int price, int like) {
        this.price = price;
        this.like = like;
    }
}
